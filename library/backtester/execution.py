import datetime
from abc import ABC, abstractmethod

from .events import Event, FillEvent, OrderEvent
from .enums import EventTypes


class ExecutionHandler(ABC):
    """
    The ExecutionHandler abstract class handles the interaction
    between a set of order objects generated by a Portfolio and
    the ultimate set of Fill objects that actually occur in the
    market.

    The handlers can be used to subclass simulated brokerages
    or live brokerages, with identical interfaces. This allows
    strategies to be backtested in a very similar manner to the
    live trading engine.
    """

    @abstractmethod
    def execute_order(self, event: OrderEvent):
        """
        Takes an Order event and executes it, producing a Fill event that gets
        placed onto the Events queue.

        Parameters:
            event - Contains an Event object with order information.
        """
        raise NotImplementedError("Should implement execute_order()")



class SimulatedExecutionHandler(ExecutionHandler):
    """
    The simulated execution handler simply converts all order objects
    into their equivalent fill objects automatically without latency,
    slippage or fill-ratio issues.

    This allows a straightforward "first go" test of any strategy,
    before implementation with a more sophisticated execution handler.
    """

    def __init__(self, events):
        """
        Parameters:
            events - The Queue of Event objects.
        """
        self.events = events

    def execute_order(self, event: Event):
        """
        Simply converts Order objects into Fill objects naively,
        i.e. without any latency, slippage or fill ratio problems.

        Parameters:
            event - Contains an Event object with order information.
        """
        if event.type != EventTypes.ORDER:
            return

        fill_event = FillEvent(
            timeindex=datetime.datetime.utcnow(), symbol=event.symbol,
            exchange='FTX', quantity=event.quantity, order_type=event.order_type,
            direction=event.direction)
        self.events.put(fill_event)